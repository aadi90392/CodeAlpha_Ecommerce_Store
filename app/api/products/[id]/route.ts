export async function GET(request: NextRequest, { params }: { params: { id: string } }) {
  try {
    // Await params to ensure it's fully resolved before accessing properties
    const { id: productId } = await params
    console.log("API: Request received for product ID:", productId) // Debug log
    const product = productDetails[productId as keyof typeof productDetails]

    if (!product) {
      console.log("API: Product not found for ID:", productId) // Debug log
      return NextResponse.json({ success: false, error: "Product not found" }, { status: 404 })
    }

    // Simulate API delay
    await new Promise((resolve) => setTimeout(resolve, 300))

    console.log("API: Found product:", product.name) // Debug log
    return NextResponse.json({
      success: true,
      product,
    })
  } catch (error) {
    console.error("API: Failed to fetch product details:", error)
    return NextResponse.json({ success: false, error: "Failed to fetch product details" }, { status: 500 })
  }
}
